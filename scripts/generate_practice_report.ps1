function Add-Paragraph {
  param(
    [Parameter(Mandatory = $true)][object]$Selection,
    [Parameter(Mandatory = $true)][string]$Text,
    [string]$Style = "Normal",
    [int]$After = 6,
    [int]$Before = 0
  )

  $Selection.Style = $Style
  $Selection.TypeText($Text)
  $Selection.TypeParagraph()
  $Selection.ParagraphFormat.SpaceAfter = $After
  $Selection.ParagraphFormat.SpaceBefore = $Before
}

function Add-Heading {
  param(
    [Parameter(Mandatory = $true)][object]$Selection,
    [Parameter(Mandatory = $true)][string]$Text,
    [string]$Style
  )
  Add-Paragraph -Selection $Selection -Text $Text -Style $Style -After 8 -Before 6
}

function Add-BulletList {
  param(
    [Parameter(Mandatory = $true)][object]$Selection,
    [Parameter(Mandatory = $true)][string[]]$Items
  )
  $Selection.Range.ListFormat.ApplyBulletDefault()
  foreach ($item in $Items) {
    $Selection.TypeText($item)
    $Selection.TypeParagraph()
  }
  $Selection.Range.ListFormat.RemoveNumbers()
  $Selection.TypeParagraph()
}

function Add-TableFromObjects {
  param(
    [Parameter(Mandatory = $true)][object]$Document,
    [Parameter(Mandatory = $true)][object]$Selection,
    [Parameter(Mandatory = $true)][string[]]$Headers,
    [Parameter(Mandatory = $true)][object[]]$Rows
  )

  $table = $Document.Tables.Add($Selection.Range, $Rows.Count + 1, $Headers.Count)
  $table.Style = "Table Grid"
  $table.Range.Font.Name = "Times New Roman"
  $table.Range.Font.Size = 12

  for ($col = 1; $col -le $Headers.Count; $col++) {
    $table.Cell(1, $col).Range.Text = $Headers[$col - 1]
    $table.Cell(1, $col).Range.Bold = 1
  }

  for ($row = 0; $row -lt $Rows.Count; $row++) {
    for ($col = 0; $col -lt $Headers.Count; $col++) {
      $table.Cell($row + 2, $col + 1).Range.Text = [string]$Rows[$row][$col]
    }
  }

  $Selection.MoveDown() | Out-Null
  $Selection.MoveDown() | Out-Null
  $Selection.TypeParagraph()
}

function Add-Figure {
  param(
    [Parameter(Mandatory = $true)][object]$Selection,
    [Parameter(Mandatory = $true)][string]$ImagePath,
    [Parameter(Mandatory = $true)][string]$Caption,
    [float]$WidthCm = 15.5
  )

  $inline = $Selection.InlineShapes.AddPicture($ImagePath)
  $inline.LockAspectRatio = $true
  $inline.Width = $Selection.Application.CentimetersToPoints($WidthCm)
  $Selection.TypeParagraph()
  $Selection.ParagraphFormat.Alignment = 1
  $Selection.Font.Italic = $true
  $Selection.TypeText($Caption)
  $Selection.Font.Italic = $false
  $Selection.TypeParagraph()
  $Selection.ParagraphFormat.Alignment = 3
}

