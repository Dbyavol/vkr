def ensure_dir(path: Path) -> None:
    path.mkdir(parents=True, exist_ok=True)


def set_document_language(document: Document, lang: str = "ru-RU") -> None:
    styles = document.styles
    for style_name in ("Normal", "Title", "Subtitle", "Heading 1", "Heading 2", "Heading 3"):
        style = styles[style_name]
        style.font.name = "Times New Roman"
        style.font.size = Pt(14 if style_name == "Normal" else 14)
        rpr = style.element.get_or_add_rPr()
        lang_el = rpr.find(qn("w:lang"))
        if lang_el is None:
            lang_el = OxmlElement("w:lang")
            rpr.append(lang_el)
        lang_el.set(qn("w:val"), lang)
        lang_el.set(qn("w:eastAsia"), lang)
        lang_el.set(qn("w:bidi"), lang)

    normal = styles["Normal"].paragraph_format
    normal.line_spacing = 1.5
    normal.first_line_indent = Cm(1.25)
    normal.space_after = Pt(0)


def set_page_layout(document: Document) -> None:
    section = document.sections[0]
    section.top_margin = Cm(2)
    section.bottom_margin = Cm(2)
    section.left_margin = Cm(3)
    section.right_margin = Cm(1.5)


def add_paragraph(document: Document, text: str, *, bold: bool = False, align=WD_ALIGN_PARAGRAPH.JUSTIFY) -> None:
    paragraph = document.add_paragraph()
    paragraph.alignment = align
    paragraph.paragraph_format.line_spacing = 1.5
    paragraph.paragraph_format.first_line_indent = Cm(1.25) if align == WD_ALIGN_PARAGRAPH.JUSTIFY else Cm(0)
    run = paragraph.add_run(text)
    run.bold = bold
    run.font.name = "Times New Roman"
    run.font.size = Pt(14)


def add_heading(document: Document, text: str, level: int) -> None:
    heading = document.add_paragraph()
    heading.style = f"Heading {min(level, 3)}"
    heading.alignment = WD_ALIGN_PARAGRAPH.CENTER if level == 1 else WD_ALIGN_PARAGRAPH.LEFT
    heading.paragraph_format.first_line_indent = Cm(0)
    run = heading.add_run(text)
    run.bold = True
    run.font.name = "Times New Roman"
    run.font.size = Pt(14)


def add_bullets(document: Document, items: list[str]) -> None:
    for item in items:
        paragraph = document.add_paragraph(style="List Bullet")
        paragraph.paragraph_format.line_spacing = 1.5
        run = paragraph.add_run(item)
        run.font.name = "Times New Roman"
        run.font.size = Pt(14)


def add_table(document: Document, headers: list[str], rows: list[list[str]]) -> None:
    table = document.add_table(rows=1, cols=len(headers))
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    table.style = "Table Grid"
    for index, header in enumerate(headers):
        cell = table.rows[0].cells[index]
        cell.text = header
        for paragraph in cell.paragraphs:
            paragraph.alignment = WD_ALIGN_PARAGRAPH.CENTER
            for run in paragraph.runs:
                run.bold = True
                run.font.name = "Times New Roman"
                run.font.size = Pt(12)

    for row in rows:
        cells = table.add_row().cells
        for index, value in enumerate(row):
            cells[index].text = value
            for paragraph in cells[index].paragraphs:
                paragraph.alignment = WD_ALIGN_PARAGRAPH.LEFT
                for run in paragraph.runs:
                    run.font.name = "Times New Roman"
                    run.font.size = Pt(12)

    document.add_paragraph()


def write_bmp(path: Path, width: int, height: int, pixels: list[tuple[int, int, int]]) -> None:
    row_stride = width * 3
    padded_stride = (row_stride + 3) & ~3
    pixel_data = bytearray()
    for y in range(height - 1, -1, -1):
        row = bytearray()
        for x in range(width):
            r, g, b = pixels[y * width + x]
            row.extend((b, g, r))
        row.extend(b"\x00" * (padded_stride - row_stride))
        pixel_data.extend(row)

    file_size = 14 + 40 + len(pixel_data)
    header = struct.pack("<2sIHHI", b"BM", file_size, 0, 0, 54)
    dib = struct.pack("<IIIHHIIIIII", 40, width, height, 1, 24, 0, len(pixel_data), 2835, 2835, 0, 0)
    path.write_bytes(header + dib + pixel_data)


def blank_canvas(width: int, height: int, color: tuple[int, int, int]) -> list[tuple[int, int, int]]:
    return [color] * (width * height)


def set_pixel(pixels: list[tuple[int, int, int]], width: int, height: int, x: int, y: int, color: tuple[int, int, int]) -> None:
    if 0 <= x < width and 0 <= y < height:
        pixels[y * width + x] = color


def draw_rect(
    pixels: list[tuple[int, int, int]],
    width: int,
    height: int,
    x1: int,
    y1: int,
    x2: int,
    y2: int,
    fill: tuple[int, int, int],
    border: tuple[int, int, int],
) -> None:
    for y in range(y1, y2):
        for x in range(x1, x2):
            set_pixel(pixels, width, height, x, y, fill)
    for x in range(x1, x2):
        set_pixel(pixels, width, height, x, y1, border)
        set_pixel(pixels, width, height, x, y2 - 1, border)
    for y in range(y1, y2):
        set_pixel(pixels, width, height, x1, y, border)
        set_pixel(pixels, width, height, x2 - 1, y, border)


def draw_line(
    pixels: list[tuple[int, int, int]],
    width: int,
    height: int,
    x1: int,
    y1: int,
    x2: int,
    y2: int,
    color: tuple[int, int, int],
    thickness: int = 2,
) -> None:
    dx = abs(x2 - x1)
    dy = -abs(y2 - y1)
    sx = 1 if x1 < x2 else -1
    sy = 1 if y1 < y2 else -1
    err = dx + dy
    while True:
        for ox in range(-thickness, thickness + 1):
            for oy in range(-thickness, thickness + 1):
                set_pixel(pixels, width, height, x1 + ox, y1 + oy, color)
        if x1 == x2 and y1 == y2:
            break
        e2 = 2 * err
        if e2 >= dy:
            err += dy
            x1 += sx
        if e2 <= dx:
            err += dx
            y1 += sy


def draw_arrow(
    pixels: list[tuple[int, int, int]],
    width: int,
    height: int,
    x1: int,
    y1: int,
    x2: int,
    y2: int,
    color: tuple[int, int, int],
) -> None:
    draw_line(pixels, width, height, x1, y1, x2, y2, color, thickness=2)
    angle = math.atan2(y2 - y1, x2 - x1)
    wing = 12
    left_x = int(x2 - wing * math.cos(angle - math.pi / 6))
    left_y = int(y2 - wing * math.sin(angle - math.pi / 6))
    right_x = int(x2 - wing * math.cos(angle + math.pi / 6))
    right_y = int(y2 - wing * math.sin(angle + math.pi / 6))
    draw_line(pixels, width, height, x2, y2, left_x, left_y, color, thickness=2)
    draw_line(pixels, width, height, x2, y2, right_x, right_y, color, thickness=2)


def draw_text(
    pixels: list[tuple[int, int, int]],
    width: int,
    height: int,
    x: int,
    y: int,
    text: str,
    color: tuple[int, int, int],
    scale: int = 3,
) -> None:
    cursor_x = x
    for char in text.upper():
        pattern = FONT_5X7.get(char, FONT_5X7[" "])
        for row_index, row in enumerate(pattern):
            for col_index, value in enumerate(row):
                if value == "1":
                    for sx in range(scale):
                        for sy in range(scale):
                            set_pixel(
                                pixels,
                                width,
                                height,
                                cursor_x + col_index * scale + sx,
                                y + row_index * scale + sy,
                                color,
                            )
        cursor_x += (5 * scale) + scale


